    private void txtLendMemberIdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtLendMemberIdFocusLost
        if (!txtLendMemberId.getText().isEmpty()) {
            tbl.setRowCount(0);
            try {
                Statement stmt = getConnection().createStatement();
                ResultSet rset = stmt.executeQuery("SELECT member_id FROM member "
                        + "WHERE member_type='" + cmbLendMemberType.getSelectedItem().toString() + "' AND "
                        + "member_id='" + txtLendMemberId.getText() + "'");
                if (rset.next()) {
                    memberOk = true;
                    loadTable();

                } else {
                    memberOk = false;
                    JOptionPane.showMessageDialog(null, "Entered user id not found from the selected user type.\n"
                            + "This user might not have register yet.");
                }
                rset.close();
                stmt.close();
            } catch (Exception ex) {
                Logger.getLogger(ManageBooksLend.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_txtLendMemberIdFocusLost

    private void txtLendBookIdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtLendBookIdFocusLost
        if (!txtLendBookId.getText().isEmpty()) {
            try {
                Statement stmt = getConnection().createStatement();
                ResultSet rset = stmt.executeQuery("SELECT book_id FROM book WHERE "
                        + "book_id='" + txtLendBookId.getText() + "'");
                if (rset.next()) {
                    bookOk = true;
                } else {
                    bookOk = false;
                    JOptionPane.showMessageDialog(null, "Entered book id not found.\n"
                            + "This book might not have register yet.");
                }
                rset.close();
                stmt.close();
            } catch (Exception ex) {
                Logger.getLogger(ManageBooksLend.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_txtLendBookIdFocusLost

    private void btnLendSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLendSaveActionPerformed
        if (bookOk && memberOk) {
            try {

                String userType = cmbLendMemberType.getSelectedItem().toString();
                Date lendDate = new Date(new java.util.Date().getTime());
                Date shouldReturnOn = new Date(dtLendReturn.getSelectedDate().getTimeInMillis());

                Statement stmt = getConnection().createStatement();
                ResultSet rset = stmt.executeQuery("SELECT lend_days, max_book FROM lend_days_fine "
                        + "WHERE member_type='" + userType + "'");

                int maxBook = 0;
                int currentBook = 0;

                if (rset.next()) {
                    Calendar c = Calendar.getInstance();
                    c.set(c.get(1), c.get(2), c.get(5) + rset.getInt(1));
                    maxBook = rset.getInt(2);
                    dtLendReturn.setSelectedDate(c);
                }
                rset = stmt.executeQuery("SELECT COUNT(*) FROM lend_return WHERE member_type='" + userType + "' AND "
                        + "member_id='" + txtLendMemberId.getText() + "' AND return_date<lend_date");
                if (rset.next()) {
                    currentBook = rset.getInt(1);
                }
                rset.close();
                stmt.close();

                if (currentBook <= maxBook) {
                    Calendar c = Calendar.getInstance();
                    c.set(c.get(1), c.get(2), c.get(5) - 100000);
                    Date returnDate = new Date(c.getTimeInMillis());

                    Lend_Return lr = new Lend_Return(userType, txtLendMemberId.getText(), txtLendBookId.getText(), lendDate, shouldReturnOn, returnDate);

                    saveData(lr);
                    loadTable();
                    JOptionPane.showMessageDialog(null, "Done");
                }else{
                    JOptionPane.showMessageDialog(null, "This member has reach max book count.");
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error occured while saving data");
                Logger.getLogger(ManageBooksLend.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Unknown member or book!");
        }
    }//GEN-LAST:event_btnLendSaveActionPerformed
