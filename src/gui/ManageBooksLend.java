    private void loadTable() {
        try {
            Statement stmt = getConnection().createStatement();
            ResultSet rset = stmt.executeQuery("SELECT b.book_id, b.title, should_return_date FROM lend_return l, book b "
                    + "WHERE member_type='" + cmbLendMemberType.getSelectedItem().toString() + "' AND "
                    + "member_id='" + txtLendMemberId.getText() + "' AND l.book_id=b.book_id");
            tbl.setRowCount(0);
            while (rset.next()) {
                ArrayList ast = new ArrayList();
                ast.add(rset.getString(1));
                ast.add(rset.getString(2));
                ast.add(rset.getDate(3));
                tbl.addRow(ast.toArray());
            }
            rset.close();
            stmt.close();
        } catch (Exception ex) {
            Logger.getLogger(ManageBooksLend.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

